#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	char		*d;
	const char	*s;
	size_t		n;
	size_t		lendest;

	d = dst;
	n = size;
	s = src;
	while (n-- != 0 && *d != '\0')
		d++;
	lendest = d - dst;
	n = size - lendest;
	if (n == 0)
		return (lendest + ft_strlen(s));
	while (*s != '\0')
	{
		if (n != 1)
		{
			*d++ = *s;
			n--;
		}
		s++;
	}
	*d = '\0';
	return (lendest + (s - src));
}
