long	ft_itob(long n)
{
	long	rem;
	long	i;
	long	binary;

	i = 1;
	binary = 0;
	while (n != 0)
	{
		rem = n % 2;
		n /= 2;
		binary += rem * i;
		i *= 10;
	}
	return (binary);
}
