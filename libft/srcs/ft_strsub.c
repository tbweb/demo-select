#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*str;
	int		i;

	i = 0;
	str = NULL;
	if (s)
	{
		str = ft_strnew(len);
		if (str != NULL)
		{
			while (len--)
			{
				str[i] = s[start + i];
				i++;
			}
			str[i] = '\0';
		}
	}
	return (str);
}
