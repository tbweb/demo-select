#include "libft.h"

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	char	*str;

	str = s1;
	while (n-- && *s2 != '\0')
	{
		*str++ = *s2++;
	}
	n++;
	while (n-- != 0)
	{
		*str++ = '\0';
	}
	return (s1);
}
